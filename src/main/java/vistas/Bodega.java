    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // INSERT INTO `proyectosi`.`material` (`Cod_material`, `nombre`, `Stock`, `Costo`, `Cantidad_fabricada`, `Stock_seguridad`) VALUES ('30', 'hierr', '9', '200', '30', '5');
        
        try{    
            PreparedStatement ps = con.prepareStatement("INSERT INTO `proyectosi`.`material` (`Cod_material`, `nombre`, `Stock`, `Costo`, `Cantidad_fabricada`, `Stock_seguridad`) VALUES (?, ?, ?, ?, ?, ?);");
            int num = 1;
            ps.setString(num++, this.txtCodMat.getText());
            ps.setString(num++, this.txtNombre.getText());
            ps.setString(num++, this.txtStock.getText());
            ps.setString(num++, this.txtCosto.getText());
            ps.setString(num++, this.txtCantFab.getText());
            ps.setString(num++, this.txtStockSeg.getText());
               
            int res = ps.executeUpdate();
            if (res > 0){
                JOptionPane.showMessageDialog(null, "Exito al guardar Material");
            }
            else
                JOptionPane.showMessageDialog(null, "Error al guardar material");

            }catch(Exception e){
                JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
            }
        
    }//GEN-LAST:event_btnRegistrarActionPerformed

  

