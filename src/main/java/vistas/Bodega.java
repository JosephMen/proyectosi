    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        //SELECT * FROM proyectosi.material;
        
        try{
            PreparedStatement ps = con.prepareStatement("SELECT * FROM proyectosi.material where Cod_material = ?;");
            ps.setString(1, this.txtCodMat.getText());
            
            ResultSet res = ps.executeQuery();           
            
            if (res.next()){
                this.txtCantFab.setText(res.getString("Cantidad_fabricada"));
                this.txtCosto.setText(res.getString("Costo"));
                this.txtNombre.setText(res.getString("nombre"));
                this.txtStock.setText(res.getString("Stock"));
                this.txtStockSeg.setText(res.getString("Stock_seguridad"));
            }
            else{
                JOptionPane.showMessageDialog(null, "No se obtuvo coincidencia");
                this.limpiar();
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
            
        }
        
    }//GEN-LAST:event_btnConsultarActionPerformed

